package trackme.business;

/**
 *
 * @author L. Andrew Overholt
 */
public class Exercise {

    // PROPERTIES
    private String name;
    private String type;

    
    
    // CONSTRUCTORS
    public Exercise(String name, String type) {
        this.name = name;
        this.type = type;
    }
    
    
    // GETTERS & SETTERS
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
    
   
    // METHODS  

    
    
    
}
